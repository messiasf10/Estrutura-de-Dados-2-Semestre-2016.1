public class Estrutura {

	public String nome;
	public int idade;
	public int cep;
	public int numero;
	public String complemento;
	
	public Estrutura(String nome, int idade, int cep, int numero, String complemento) {
		super();
		this.nome = nome;
		this.idade = idade;
		this.cep = cep;
		this.numero = numero;
		this.complemento = complemento;
	}
	
}
